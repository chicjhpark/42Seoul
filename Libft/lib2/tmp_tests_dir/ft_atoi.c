#include "libft.h"

int		ft_atoi(const char *str)
{
	int		temp;
	int		sign;
	size_t	i;

	temp = 0;
	sign = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (sign)
			temp = temp * 10 - (str[i] - '0');
		else
			temp = temp * 10 + (str[i] - '0');
		i++;
	}
	return (temp);
}
