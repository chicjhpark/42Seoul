#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	i;
	size_t	count;

	count = strlen(s1);
	if (!(s2 = (char *)malloc(sizeof(char *) * count)))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = 0;
	free(s2);
	return (s2);
}
